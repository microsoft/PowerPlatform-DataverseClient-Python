# Simple logging helper
def log_call(description):
    print(f"\n-> {description}")


# Define enum for priority picklist
class Priority(IntEnum):
    LOW = 1
    MEDIUM = 2
    HIGH = 3


# ---------------------------------------------------------------------------
# Typed entity classes
#
# These mirror the Dataverse tables used in this walkthrough.
# In a real project these would be auto-generated by the generate() tool.
# Field names are the OData logical names (lowercase).
# ---------------------------------------------------------------------------

class WalkthroughDemo(Entity, table="new_WalkthroughDemo", primary_key="new_walkthroughdemoid"):
    """Typed entity for the new_WalkthroughDemo custom table."""
    new_walkthroughdemoid: str
    new_title:             str
    new_quantity:          int
    new_amount:            float
    new_completed:         bool
    new_notes:             str
    new_priority:          int


class Account(Entity, table="account", primary_key="accountid"):
    """Typed entity for the built-in account table (used in expand demo)."""
    accountid:        str
    name:             str
    # When .expand("primarycontactid") is used, this holds the nested contact
    # dict rather than the lookup ID string.
    primarycontactid: str
    # Navigation collection — populated when .expand("Account_Tasks") is used.
    Account_Tasks:    list


def backoff(op, *, delays=(0, 2, 5, 10, 20, 20)):
    last = None
    total_delay = 0
    attempts = 0
    for d in delays:
        if d:
            time.sleep(d)
            total_delay += d
        attempts += 1
        try:
            result = op()
            if attempts > 1:
                retry_count = attempts - 1
                print(f"   [INFO] Backoff succeeded after {retry_count} retry(s); waited {total_delay}s total.")
            return result
        except Exception as ex:  # noqa: BLE001
            last = ex
            continue
    if last:
        if attempts:
            retry_count = max(attempts - 1, 0)
            print(f"   [WARN] Backoff exhausted after {retry_count} retry(s); waited {total_delay}s total.")
        raise last


