"""
Walkthrough demonstrating core Dataverse SDK operations using strongly typed entities.

This is the typed counterpart to walkthrough.py.  Every section performs the
same operations as the original — the only difference is how those operations
are expressed:

  String-based (original)           Typed (this file)
  ─────────────────────────────     ──────────────────────────────────────────
  builder("new_WalkthroughDemo")    builder(WalkthroughDemo)
  .select("new_Title", ...)         .select(WalkthroughDemo.new_title, ...)
  .filter_eq("new_Completed", 0)    .where(WalkthroughDemo.new_completed == False)
  record.get("new_title")           demo.new_title
  {"new_Title": "x", ...}           WalkthroughDemo(new_title="x", ...)

Under the hood both paths compile to identical OData requests.  No changes
were made to the HTTP layer, auth, or data layer.

This example shows:
- Defining a typed entity class that mirrors a Dataverse table
- Single and multiple record CRUD using WalkthroughDemo(...)
- Typed attribute access on query results (demo.new_title vs record.get())
- Querying with typed field expressions (WalkthroughDemo.statecode == 0)
- Composable typed filter conditions (&, |, ~)
- Paging, to_dataframe(), expand, SQL, batch — all still work unchanged

Prerequisites:
- pip install PowerPlatform-Dataverse-Client
- pip install azure-identity
"""

